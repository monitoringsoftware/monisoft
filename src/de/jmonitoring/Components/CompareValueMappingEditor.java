    /**
     * Für eine neue Verbrauchskategorie zur Liste hinzu
     *
     * @param cv
     */
    private void addEntry(SensorCollection cv) {
        // Prüfen ob Name schon vorhanden....
        for (int i = 0; i < entryHolder.getComponentCount(); i++) {
            if (cv.getName().equals(((SensorCollection) ((SensorCollectionPanel) entryHolder.getComponent(i)).getCompareValue()).getName())) {
                JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("de/jmonitoring/Components/Bundle").getString("CompareValueMappingEditor.EINE VERBRAUCHSGRUPPE MIT DIESEM NAMEN IST BEREITS VORHADEN."));
                return;
            }
        }

        // Wenn nicht vorhanden neues Panel bilden und hinzufügen
        SensorCollectionPanel entryPanel = new SensorCollectionPanel(cv, SensorCollectionHandler.COMPARE_COLLECTION); // neues Panel erzeugen
        entryPanel.initSensors(jScrollPane2, true);
//        entryPanel.sort();
        entryHolder.add(entryPanel);
        entryHolder.doLayout();
        entryHolder.revalidate();
        SwingUtilities.invokeLater(new Runnable() {
            @Override
            public void run() {
                jScrollPane2.getVerticalScrollBar().setValue(jScrollPane2.getVerticalScrollBar().getModel().getMinimum());
            }
        });
        jScrollPane2.revalidate();

        if (active) {
            changed = true;
            saveButton.setEnabled(true);
        }
    }

    public void doCascade(boolean b) {
        // alle einklappen
        for (int i = 0; i < entryHolder.getComponentCount(); i++) {
            ((SensorCollectionPanel) entryHolder.getComponent(i)).doCascade(b);
        }
    }

    public void removeEntry(SensorCollectionPanel panel) {
        entryHolder.remove(panel);
        collectionDeleteList.add(panel.getTitle());
        panel = null;
        entryHolder.doLayout();
        entryHolder.revalidate();
        SwingUtilities.invokeLater(new Runnable() {
            @Override
            public void run() {
                jScrollPane2.getVerticalScrollBar().setValue(jScrollPane2.getVerticalScrollBar().getModel().getMinimum());
            }
        });
        jScrollPane2.revalidate();
        if (active) {
            changed = true;
            saveButton.setEnabled(true);
        }
    }

    public void setChangedState(boolean b) {
        changed = b;
        saveButton.setEnabled(b);
    }

    public void renameCollection(String oldName, String newName) {
        if (SensorCollectionHandler.getCollectionID(oldName) != null) {
            renamedNames.put(newName, SensorCollectionHandler.getCollectionID(oldName));
        }
    }

    /**
     * Belegt die Kategorienliste mit den kategorien aus der DB
     */
    private void populateGroupList() {
        Map<String, ArrayList<Integer>> map = SensorCollectionHandler.getCollectionsOfCreator(SensorCollectionHandler.COMPARE_COLLECTION);
        Iterator<String> it = map.keySet().iterator();
        String name;
        ArrayList<Integer> list;
        SensorCollection cv;
        while (it.hasNext()) {
            name = it.next();
            cv = new SensorCollection(name);
            list = map.get(name);
            for (Integer id : list) {
                cv.addSensor(SensorInformation.getSensorProperties(id));
            }
            addEntry(cv);
        }
        jScrollPane2.revalidate();
        active = true;
    }

    /**
     *
     * @param tree
     * @param parent
     * @param expand
     */
    private void expandAll(JTree tree, TreePath parent, boolean expand) {
        // Traverse children
        TreeNode node = (TreeNode) parent.getLastPathComponent();
        if (node.getChildCount() >= 0) {
            for (Enumeration e = node.children(); e.hasMoreElements();) {
                TreeNode n = (TreeNode) e.nextElement();
                TreePath path = parent.pathByAddingChild(n);
                expandAll(tree, path, expand);
            }
        }

        // Expansion or collapse must be done bottom-up
        if (expand) {
            tree.expandPath(parent);
        } else {
            tree.collapsePath(parent);
        }
        jScrollPane2.revalidate();
    }
