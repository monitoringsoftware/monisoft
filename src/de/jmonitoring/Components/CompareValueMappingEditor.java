    private void saveButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveButtonActionPerformed
        HashSet<SensorProperties> sensorProperties;
        String collectionName;

        // delete collections
        for (String deleteName : collectionDeleteList) {
//            System.out.println("deleting collection: " + deleteName);
            SensorCollectionHandler.removeCollection(deleteName, false);
        }
        SensorCollectionHandler.refreshCollections();
        collectionDeleteList.clear();

        // loop all Sensorcollections 
        for (int i = 0; i < entryHolder.getComponentCount(); i++) {
            collectionName = ((SensorCollectionPanel) entryHolder.getComponent(i)).getTitle();

            // get all sensors in current collection
            HashSet<Integer> list = new HashSet<Integer>();
            sensorProperties = ((SensorCollection) ((SensorCollectionPanel) entryHolder.getComponent(i)).getCompareValue()).getPropertySet();

            // loop all sensors in current collection
            for (SensorProperties p : sensorProperties) {
                list.add(p.getSensorID()); // und die SensorProperties in Liste aufnehmen
            }
            Integer collectionID = SensorCollectionHandler.getCollectionID(collectionName);
            if (collectionID != null || renamedNames.containsKey(collectionName)) { // a collection with that name already exists or it was renamed: update it
                if (collectionID == null) {
                    collectionID = renamedNames.get(collectionName);
                }
                SensorCollectionHandler.updateCollection(collectionID, collectionName, list, ((SensorCollectionPanel) entryHolder.getComponent(i)).isClimateCorrectable(), false);
            } else { // this is a new collection: create it
                SensorCollectionHandler.insertCollection(collectionName, list, SensorCollectionHandler.COMPARE_COLLECTION, ((SensorCollectionPanel) entryHolder.getComponent(i)).isClimateCorrectable(), false);
            }
        }
        changed = false;
        saveButton.setEnabled(false);
        dispose();
    }//GEN-LAST:event_saveButtonActionPerformed

