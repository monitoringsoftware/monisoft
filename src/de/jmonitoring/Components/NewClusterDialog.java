    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        if (ClusterInformation.clusterExists(clusterNameTextField.getText().trim())) {
            messageLabel.setText("Ein Cluster mit diesem Namen existiert schon.");
            return;
        }

        cluster = new Cluster(clusterNameTextField.getText().trim());
        Integer groupCount = Integer.valueOf((String) groupCountCombobox.getSelectedItem());
        for (int i = 1; i <= groupCount; i++) {
            cluster.addGroup(i);
        }
        setVisible(false);
    }//GEN-LAST:event_okButtonActionPerformed

