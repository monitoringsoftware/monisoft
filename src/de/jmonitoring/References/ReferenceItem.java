    /**
     * Creatze a new panel useing the given {@link ReferenceValue}
     *
     * @param reference The {@link ReferenceValue}for this panel
     * @param caller The calling {@link BuildingEditorDialog}
     */
    public ReferenceItem(ReferenceValue reference, BuildingEditorDialog caller) {
        this.caller = caller;
        this.reference = reference;
        initComponents();
        if (this.reference != null) {
            nameLabel.setText(this.reference.getName());
            valueLabel.setText(String.valueOf(this.reference.getValue())); // TODO mit Decimalformat!
            unitLabel.setText(UnitInformation.getUnitNameFromID(ReferenceInformation.getUnitIDForReference(this.reference.getName())));
            if (caller != null) {
                jButton1.setEnabled(caller.isEditable());
            }
        }
    }

    /**
     * Return this panels {@link ReferenceValue}
     *
     * @return The {@link ReferenceValue}
     */
    public ReferenceValue getReference() {
        return this.reference;
    }

