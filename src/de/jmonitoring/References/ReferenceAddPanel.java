    public ReferenceAddPanel(boolean editable, BuildingEditorDialog bd) {
        initComponents();
        this.caller = bd;
        valueTextField.setEnabled(editable);
        referencesCombobox.setEnabled(editable);
        DefaultComboBoxModel model = new DefaultComboBoxModel();
        for (ReferenceDescription ref : ReferenceInformation.getReferenceList()) {
            model.addElement(ref.getName());
        }
        referencesCombobox.setModel(model);

        unitLabel.setText(UnitInformation.getUnitNameFromID(ReferenceInformation.getUnitIDForReference(((String) referencesCombobox.getSelectedItem()))));
    }

    /**
     * Set the fields according to the given {@link ReferenceValue}
     *
     * @param reference The reference value
     */
    public void setFields(ReferenceValue reference) {
        referencesCombobox.setSelectedItem(reference.getName());
        valueTextField.setText(String.valueOf(reference.getValue()));
    }

    /**
     * Build a {@link ReferenceValue} out of the dialogs fields
     *
     * @return The new {@link ReferenceValue} 
     */
    public ReferenceValue getReferenceFields() {
        // Prüfen ob gültiger Wert > 0
        if (valueTextField.getText().trim().isEmpty() || new ConvertStringToDouble().convert(valueTextField.getText()) <= 0d) {
            return null;
        }

        Integer buildingID;
        if (caller.getCurrentProperties().getBuildingID() == 0) {
            buildingID = null;
        } else {
            buildingID = caller.getCurrentProperties().getBuildingID();
        }

        return new ReferenceValue((String) referencesCombobox.getSelectedItem(), new ConvertStringToDouble().convert(valueTextField.getText()), buildingID, null);
    }

