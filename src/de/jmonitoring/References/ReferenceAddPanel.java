    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (getReferenceFields() != null) { // wenn es eine gültige Bezugsgröße ist
            caller.getReferenceItemHolder().addReference(getReferenceFields());
            caller.updateScrollPane(BuildingEditorDialog.BOTTOM);
        } else { // sonst warnen
            JOptionPane.showMessageDialog(this, java.util.ResourceBundle.getBundle("de/jmonitoring/Components/Bundle").getString("ReferenceAddPanel.NOREFVALUEMSG"), java.util.ResourceBundle.getBundle("de/jmonitoring/Components/Bundle").getString("ReferenceAddPanel.NOREFVALUE"), JOptionPane.WARNING_MESSAGE);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

